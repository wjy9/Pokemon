function getPokemonName( number, form ) {
	form = form || true;
	if ( number.length = 6 ) {
		var name = Pokemon_Names[parseInt(number.split('.')[0])];
		if ( form && number in Pokemon_Forms ) {
			var form = Pokemon_Forms[number];
			if ( form.indexOf(name) == -1 ) form = name + ' ' + form;
			return form;
		} else {
			return name;
		}
	} else {
		return Pokemon_Names[parseInt(number)];
	}
}